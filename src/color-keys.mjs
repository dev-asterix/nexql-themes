/**
 * Pairs to validate for WCAG contrast.
 * @type {Array<{ fg: string, bg: string, min: number, label: string }>}
 */
export function contrastPairs(colors, type) {
  const isHc = type === "hc";
  const textMin = isHc ? 7 : 4.5;
  const uiMin = isHc ? 4.5 : 3;

  return [
    { fg: "foreground", bg: "editor.background", min: textMin, label: "editor text" },
    { fg: "foreground", bg: "sideBar.background", min: textMin, label: "sidebar text" },
    { fg: "list.activeSelectionForeground", bg: "list.activeSelectionBackground", min: uiMin, label: "list selection" },
    { fg: "statusBar.foreground", bg: "statusBar.background", min: uiMin, label: "status bar" },
    { fg: "button.foreground", bg: "button.background", min: uiMin, label: "button" },
    { fg: "input.foreground", bg: "input.background", min: textMin, label: "input" },
    { fg: "tab.activeForeground", bg: "tab.activeBackground", min: uiMin, label: "active tab" },
  ];
}
